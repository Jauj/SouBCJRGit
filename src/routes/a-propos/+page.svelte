  <h1>À propos</h1>

  <h2>Qui sommes-nous ?</h2>
  <p>Le <strong>Cercle de Jeunes Révolutionnaires (CJR)</strong> est une organisation politique de jeunesse qui se consacre à l'étude, à la diffusion et à la mise en pratique des principes du socialisme révolutionnaire. Notre objectif est de former des cadres politiques capables d'analyser les contradictions du capitalisme contemporain et de porter une alternative émancipatrice.</p>

  <h2>Ce bulletin</h2>
  <p><em>Socialisme ou Barbarie</em> est le bulletin de liaison du CJR. Il publie des analyses politiques, des thèses théoriques, des comptes rendus de discussions et des contributions sur les enjeux stratégiques du mouvement ouvrier et des luttes populaires. Chaque texte est le fruit d'un travail collectif de réflexion et de débats internes.</p>

  <p>Ce bulletin n'est pas un organe de propagande éditorialisé : il s'agit d'un espace de travail politique où les idées sont mises à l'épreuve de la critique et de la pratique. Les positions exprimées engagent le collectif et sont soumises à révision à la lumière de l'expérience et de l'analyse concrète des situations.</p>

  <h2>Nos principes</h2>
  <p>Nous nous revendiquons de la tradition du marxisme révolutionnaire, entendu non comme un dogme figé mais comme une méthode d'analyse dialectique en constante évolution. Nous considérons que la théorie doit rester intimement liée à la pratique, et que toute abstraction qui perd le contact avec le mouvement réel des masses se condamne à l'impuissance.</p>

  <p>Nos activités comprennent l'étude régulière des classiques du marxisme, l'analyse de l'actualité politique et économique, l'intervention dans les luttes sociales, et la formation politique de nos membres et sympathisants.</p>

  <h2>Nous contacter</h2>
  <p>Pour toute question, contribution ou demande d'information, n'hésitez pas à nous écrire. Le CJR est ouvert à toute personne partageant notre engagement pour l'émancipation des travailleurs et des peuples.</p>
